function getDataFromPlanet(planet, category){
	var field;
	for (var i = 0; i < planetsVizData.length; i++){
		if (planetsVizData[i].Nom == category) {
			for (index in planetsVizData[i]){
				if (index == planet) return planetsVizData[i][index];
			}
		}
	}
}

function getDataFromCategory(category, labels){
	var result = [];
	for (var i = 0; i < planetsVizData.length; i++){
		if  (planetsVizData[i].Nom == category) {
			for (index in planetsVizData[i]){
				if (index != 'Nom' && labels == true) result.push( [index, planetsVizData[i][index]] )
				else if (index != 'Nom' && labels == false) result.push( planetsVizData[i][index] ) 
			}
		}
	}
	return result;
}

function getLabels(){
	var result = [];
	for (index in planetsVizData[0]){
		if (index != 'Nom') result.push( index )
	}
	return result;
}


